<?php
// Mendefinisikan jari-jari lingkaran
$jari_jari = 15; // cm

// Mendefinisikan nilai π (pi)
$pi = 3.14159;

// Menghitung keliling lingkaran
$keliling = 2 * $pi * $jari_jari;

// Menampilkan hasil keliling lingkaran beserta jari-jari dan nilai pi
echo "Diketahui:<br>";
echo "- Jari-jari lingkaran = " . $jari_jari . " cm<br>";
echo "- Nilai π (pi) = " . $pi . "<br><br>";

echo "Maka:<br>";
echo "Keliling lingkaran dengan jari-jari " . $jari_jari . " cm adalah: " . $keliling . " cm";
?>
